<template>
  <ValidationProvider :vid="vid" :name="$attrs.label" :rules="rules" v-slot="{ errors }">
    <!-- 隐藏label -->
    <el-form-item v-if="typeof $attrs.hideLabel == 'string'" :error="errors[0]" :label-width="$attrs['label-width']">
      <el-input v-if="editable" v-bind="$attrs" v-model="innerValue" @change="change">
        <span slot="suffix">{{unit}}</span>
      </el-input>
      <span v-if="!editable">{{innerValue}}{{unit}}</span>
    </el-form-item>
    <!-- 显示label -->
    <el-form-item v-else :error="errors[0]" :label="(isRequired ? '*' : '' ) + $attrs.label + ':'" :label-width="$attrs['label-width']">
      <el-input v-if="editable" v-bind="$attrs" v-model="innerValue" @change="change">
        <span slot="suffix">{{unit}}</span>
      </el-input>
      <span v-if="!editable">{{innerValue}}{{unit}}</span>
    </el-form-item>
  </ValidationProvider>
</template>

<script>
export default {
  props: {
    vid: {
      type: String
    },
    rules: {
      type: [Object, String],
      default: ""
    },
    // must be included in props
    value: {
      type: null
    },
    editable: {
      type: Boolean,
      default: true
    },
    change: {
      type: Function,
      default: function() {}
    },
    unit: String
  },
  data: () => ({
    innerValue: "",
    isRequired: false
  }),
  watch: {
    // Handles internal model changes.
    innerValue(newVal) {
      this.$emit("input", newVal);
    },
    // Handles external model changes.
    value(newVal) {
      this.innerValue = newVal;
    }
  },
  created() {
    if(Object.prototype.toString.call(this.rules) == "[object String]"){
        this.isRequired =  this.rules.includes('required');
    }
    // console.log("$attrs.label:",  this.$attrs.label,this.rules);
    if (this.value) {
      this.innerValue = this.value;
    }
  }
};
</script>
